<a name="mnd_2jc_jcb"></a>
<h2>Tarifa administrativa de reembolso</h2>

  
<div>
    
    
    
    
    
<p>Se você reembolsar um cliente por um pedido pelo qual você já tenha recebido pagamento, a Amazon reembolsará a você a comissão que você pagou pelo(s) item(ns), menos a Tarifa administrativa de reembolso, que é o menor valor de R$&nbsp;10,00 ou 20% da comissão aplicável.</p>

    
    
    
    
    
    
<p>Por exemplo, caso você tenha reembolsado um cliente o valor total de R$&nbsp;10,00 de uma venda em uma categoria cuja comissão é de 15%, sua Tarifa administrativa de reembolso será de R$&nbsp;0,30 (20% de R$&nbsp;1,50, que era a comissão original de 15%).</p>

  
</div>



</div>






