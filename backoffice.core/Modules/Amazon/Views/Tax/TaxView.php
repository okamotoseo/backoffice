<h2>Tarifas de vendas</h2>

  
<div>
    
    
    
    
    
<p>Quando você vende um item, a Amazon coleta a quantia paga pelo cliente (incluindo o preço do item, as cobranças de envio, embalagem para presente e outras). As taxas de envio da Amazon se aplicam a Livros vendidos por vendedores Profissionais e também a todos os produtos vendidos por vendedores Individuais. Apenas vendedores Profissionais podem oferecer embalagem para presente.</p>

    
    
  
</div>



